using namespace std;
template <class T>
class CArray3D
{
// 在此处补充你的代码
public:
	class CArray2DArray {
	public:
		T* data;
		int pos;
		int sizem;
		int sizen;
		CArray2DArray(int sa=0, int sm=0, int sn=0):sizem(sm), sizen(sn), pos(0) {data = new T[sa*sm*sn];}
		T* operator [] (int idx) {return data + pos*sizem*sizen + idx*sizen;}
		operator T*() {return data;}
	} C3D;
	CArray3D(int sa, int sb, int sc): C3D(CArray2DArray(sa, sb, sc)) {}
	CArray2DArray& operator [] (int idx) {C3D.pos = idx; return C3D;}
	operator void*() {return C3D.data;}
};

CArray3D<int> a(3,4,5);
CArray3D<double> b(3,2,2);
void PrintA()
{
    for(int i = 0;i < 3; ++i) {
        cout << "layer " << i << ":" << endl;
        for(int j = 0; j < 4; ++j) {
            for(int k = 0; k < 5; ++k)
                cout << a[i][j][k] << "," ;
            cout << endl;
        }
    }
}
void PrintB()
{
    for(int i = 0;i < 3; ++i) {
        cout << "layer " << i << ":" << endl;
        for(int j = 0; j < 2; ++j) {
            for(int k = 0; k < 2; ++k)
                cout << b[i][j][k] << "," ;
            cout << endl;
        }
    }
}

int main()
{
    
    int No = 0;
    for( int i = 0; i < 3; ++ i )
        for( int j = 0; j < 4; ++j )
            for( int k = 0; k < 5; ++k )
                a[i][j][k] = No ++;
    PrintA();
    memset(a, -1, 60 * sizeof(int));        //注意这里
    memset(a[1][1], 0, 5 * sizeof(int));
    PrintA();
    
    for( int i = 0; i < 3; ++ i )
        for( int j = 0; j < 2; ++j )
            for( int k = 0; k < 2; ++k )
                b[i][j][k] = 10.0 / (i + j + k + 1);
    PrintB();
    int n = a[0][1][2];
    double f = b[0][1][1];
    cout << "****" << endl;
    cout << n << "," << f << endl;
    
    return 0;
}